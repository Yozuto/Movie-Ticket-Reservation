    /**
     * This method is used to perform the operations in application 
     * @param evt 
     */
    private void btnClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnClick
        JButton clickedBtn = (JButton)evt.getSource(); // Identifies the clicked button in the appication 
        
        int price = (int) movieInfo.getSelectedMoviePrice();
        Color green = new Color(0,255,0); // Creating green color
        Color gray = new Color(102,102,102); // Creating gray color
        if(clickedBtn.getBackground().equals(gray)){ // If an user clicked gray button
            if(clickedBtn.getName().contains("R1")){ // If user clicked a last row gray button
                this.total += price; // Increase total cost by 20
            }
            else{ // If user clicked other gray button
                this.total += price; // Increase total cost by 12
            }
            clickedBtn.setBackground(green); // Setting clicked button background to green
            this.ticketCount += 1; // Increase ticket count by 1
            this.totalCounter.setText(": " + Integer.toString(total) + " PHP"); // Display total in specified label
            this.seatCounter.setText(seatCounter.getText().concat(clickedBtn.getName()) + "  ");  // Display selected seats in specified label
            this.selection.add(clickedBtn); // Add selected button to list
        }
        else if(clickedBtn.getBackground().equals(green)){ // If an user clicked green button
            if(clickedBtn.getName().contains("R1")){ // If user clicked a last row green button
                this.total -= price; // Decrease total cost by 20
            }
            else{ // If user clicked other green button
                this.total -= price;  // Decrease total cost by 12
            }
            clickedBtn.setBackground(gray); // Setting clicked button background to gray
            this.ticketCount -= 1; // Decrease ticket count by 1
            this.totalCounter.setText(": " + Integer.toString(total) + " PHP"); // Display total in specified label
            this.seatCounter.setText(seatCounter.getText().replaceAll(clickedBtn.getName() + "  ", "")); // Remove selected seats from specified label
            this.selection.remove(clickedBtn); // Remove clicked button from the list
        }
        this.ticketCounter.setText(": " + Integer.toString(ticketCount)); // Display amount of tickets in specified label
        
    }//GEN-LAST:event_btnClick
    
