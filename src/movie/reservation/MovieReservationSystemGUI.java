    public void bookSeatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookSeatsActionPerformed
        
        Color red = new Color(255, 0, 0);
        TicketInformation ticket = new TicketInformation(); // Creating new ticket to show ticket infromati
        ticket.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
         // Stop execution if the frame is closed
        
        passInfo(this.nameLbl.getText(), this.timeLbl.getText(), this.ticketCounter.getText(), this.totalCounter.getText(), this.seatCounter.getText()); // Pass tickect information to second frame(Ticket information)
        ticket.setVisible(true); 
        
        this.selection.forEach(selectedBtns -> {
        
        selectedBtns.setBackground(red);
        this.ticketCounter.setText(":");
        this.totalCounter.setText(":");
        this.seatCounter.setText(":");
        this.ticketCount = 0;
        this.total = 0;
        if (this.selection.size() == 84) {
            this.bookSeats.setEnabled(false);
        }
    });

        
        
    }

 
       
    
